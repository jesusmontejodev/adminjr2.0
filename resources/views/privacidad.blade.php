    <h2 class="text-xl font-semibold">2. GLOSARIO</h2>

    <p><strong>Datos Personales:</strong> Cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables.</p>

    <p><strong>Datos Personales Privados:</strong> Aquellos cuyo conocimiento es restringido al público.</p>

    <p><strong>Datos Públicos:</strong> Dato que no sea semiprivado, privado o sensible, que puede ser tratado por cualquier persona, sin necesidad de autorización para ello.</p>

    <p><strong>Titular de la Información:</strong> Persona natural cuyos datos personales sean objeto de Tratamiento.</p>

    <p><strong>Causahabiente:</strong> Persona que ha sucedido a otra por causa del fallecimiento de ésta (heredero o legatario).</p>

    <p><strong>Base de Datos:</strong> Conjunto organizado de datos personales que sea objeto de Tratamiento.</p>

    <p><strong>Tratamiento de Datos:</strong> Cualquier operación o conjunto de operaciones sobre datos personales.</p>

    <p><strong>Responsable del Tratamiento:</strong> Persona natural o jurídica que decida sobre la base de datos.</p>

    <p><strong>Encargado del Tratamiento:</strong> Persona que realice el Tratamiento por cuenta del Responsable.</p>

    <p><strong>Autorización:</strong> Consentimiento previo, expreso e informado del Titular.</p>

    <p><strong>Aviso de Privacidad:</strong> Comunicación que informa al titular sobre el tratamiento de su información.</p>

    <p><strong>Transferencia:</strong> Envío de datos dentro o fuera del territorio nacional.</p>

    <p><strong>Oficial de Protección de Datos:</strong> Encargado de vigilar la aplicación de la política de datos.</p>

