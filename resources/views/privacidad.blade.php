    <h2 class="text-xl font-semibold">
        5. El intercambio o transferencia de datos personales
    </h2>

    <p>
        “AVASPACE” no transfiriere a terceros los datos de los clientes sin el consentimiento
        de los titulares, salvo las excepciones previstas en el Artículo 37 de la Ley de Protección
        de Datos Personales en Posesión de Particulares, así como a realizar la transferencia
        en los términos que fija la ley.
    </p>

    <p class="font-semibold">
        Artículo 37. Las transferencias nacionales o internacionales de datos podrán
        llevarse a cabo sin el consentimiento del titular cuando se dé alguno de los
        siguientes supuestos:
    </p>

    <ol class="list-decimal pl-6 space-y-1">
        <li>
            Cuando la transferencia esté prevista en una Ley o Tratado en los que
            México sea parte.
        </li>
        <li>
            Cuando la transferencia sea necesaria para la prevención o el diagnóstico
            médico, la prestación de asistencia sanitaria, tratamiento médico o la
            gestión de servicios sanitarios;
        </li>
        <li>
            Cuando la transferencia sea efectuada a sociedades controladoras,
            subsidiarias o afiliadas bajo el control común del responsable, o a una
            sociedad matriz o a cualquier sociedad del mismo grupo del responsable
            que opere bajo los mismos procesos y políticas internas;
        </li>
        <li>
            Cuando la transferencia sea necesaria por virtud de un contrato celebrado
            o por celebrar en interés del titular, por el responsable y un tercero;
        </li>
        <li>
            Cuando la transferencia sea necesaria o legalmente exigida para la
            salvaguarda de un interés público, o para la procuración o administración
            de justicia;
        </li>
        <li>
            Cuando la transferencia sea precisa para el reconocimiento, ejercicio o
            defensa de un derecho en un proceso judicial.
        </li>
        <li>
            Cuando la transferencia sea precisa para el mantenimiento o cumplimiento
            de una relación jurídica entre el responsable y el titular.
        </li>
    </ol>

